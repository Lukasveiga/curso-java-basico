package com.lukas.aula11;

public class VariaveisChar {

    public static void main(String[] args) {
        
        char o = 'o';
        char i = 'i';

        char oAscii = 111;
        char iAscii = 105;

        System.out.println("" + o + i);
        System.out.println("" + oAscii + iAscii);
    }
    
}
