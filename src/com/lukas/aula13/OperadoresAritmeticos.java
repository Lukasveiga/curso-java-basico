package com.lukas.aula13;

public class OperadoresAritmeticos {

    public static void main(String[] args) {
        
        int resultado = 1 + 2;
        System.out.println(resultado);

        // adicionando 1
        System.out.println(resultado++);  // > first print the result and after add +1

        
        System.out.println(++resultado); // frist add +1 in the result and after print the result
    }
    
}
