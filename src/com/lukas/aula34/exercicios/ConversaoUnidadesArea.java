package com.lukas.aula34.exercicios;

public class ConversaoUnidadesArea {

    public static double pes2ParaM2(double valor) {
        return valor/10.76;
    }

    public static double cm2ParaPe2(double valor) {
        return valor/929;
    }

    public static double acresParaMilha2(double valor) {
        return valor/640;
    }

    public static double Pes2ParaAcre(double valor) {
        return valor/43560;
    }
    
}
